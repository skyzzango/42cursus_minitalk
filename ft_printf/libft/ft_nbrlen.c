#include "libft.h"

size_t	ft_nbrlen(int nbr)
{
	size_t	i;
	t_uint	n;

	i = 1;
	if (0 <= nbr)
		n = nbr;
	else
	{
		n = nbr * -1;
		i++;
	}
	while (10 <= n)
	{
		n /= 10;
		i++;
	}
	return (i);
}
